function renderProducts() {
    const contentProducts = document.getElementById("contentProduct");

    products.forEach((product, index) => {
        // Agregar condici√≥n para filtrar por categor√≠a "brasil" o "nikeAir"
        if (product.category !== "nikeAir") {
            return; // Omitir este producto si no es de la categor√≠a "brasil" ni "nikeAir"
        }

        const productDiv = document.createElement("div");
        productDiv.classList.add("product");
        productDiv.dataset.index = index;
        
        // Configurar el fondo del div directamente
        const img = document.createElement("img");
        img.src = product.img;
        img.alt = "product img";

        const checkbox = document.createElement("input");
        checkbox.type = "checkbox";
        checkbox.style.opacity = 0;

        const title = document.createElement("h2");
        title.textContent = product.title;

        const description = document.createElement("p");
        description.textContent = product.description;

        const whatsapp = document.createElement("a");
        whatsapp.href = `${product.whatsapp}&text=${encodeURIComponent("Hola Ind. Ferechian, quiero saber que talles tenes en stock y que colores de este par : " + product.title + " " + product.description)}`;

        const whatsappButton = document.createElement("img");
        whatsappButton.src = '../Images/logowhatsapp.webp'; // Coloca aqu√≠ la URL del logotipo de WhatsApp
        whatsappButton.alt = "WhatsApp";
        whatsappButton.style.width = "40px";
        whatsappButton.style.height = "40px";

        const whatsappText = document.createElement("span");
        whatsappText.textContent = "Cons√∫ltanos a nuestro WhatsApp";

        whatsapp.appendChild(whatsappText);
        whatsapp.appendChild(whatsappButton);

        productDiv.appendChild(img);
        productDiv.appendChild(title);
        productDiv.appendChild(description);
        productDiv.appendChild(whatsapp);
        // Agregar el div del producto al contenedor principal
        contentProducts.appendChild(productDiv);

        // Agregar un manejador de eventos al div del producto para cambiar el estado del checkbox
        productDiv.addEventListener("click", () => {
            window.location.href = `./product.html?productId=${index}`;
            console.log(`Clic en el producto ${index}`);
            // Puedes agregar aqu√≠ la l√≥gica adicional que necesites
        });
    });
}

// Llamada a la funci√≥n renderProducts
document.addEventListener("DOMContentLoaded", renderProducts);
